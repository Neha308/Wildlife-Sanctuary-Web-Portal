    	<b>5.Jim Corbett National Park</b>At a distance of 4 km from Ramnagar Railway Station, 65 km from Nainital, 232 km from Dehradun, and 261 km from Delhi, Jim Corbett National Park is India's oldest and most popular National Park situated in Nainital district of Uttarakhand. The Park acts as a protection zone for Bengal Tigers of India. It is one of the wellknown Uttarakhand tourist places and among the best places to visit near Delhi.It is one of the best managed and protected areas in the country and attracts thousands of Indian and Foreign visitors each year. It is home to around 50 species of trees, 580 bird species and 25 reptile species. The park is also home to wide variety of wildlife including Tigers, Elephants, Chital, Sambar Deer, Nilgai, Gharial, King Cobra, Muntjac, Wild boar, Hedgehog, common Musk Shrew, Flying fox and Indian Pangolin.<br>
